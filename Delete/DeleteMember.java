    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if ( txtID.getText().trim().length() == 0 ) {
            JOptionPane.showMessageDialog(this, "Insert the ID!!");
            return;
        }
        try {
            String ID = txtID.getText();
            pst = con.prepareStatement("DELETE FROM member WHERE id=?");
            pst.setString(1, ID);

            int k = pst.executeUpdate();

            if (k==1) {
                JOptionPane.showMessageDialog(this, "Record Deleted!!");
                txtID.setText("");
            }
            else {
                JOptionPane.showMessageDialog(this, "Record Failed to Deleted!!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(DeleteBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
